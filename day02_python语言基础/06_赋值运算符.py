# =,+=,-=,*=,/=,//=,%=,**=
#创建变量（将变量指向值，将值赋予变量)
num = 10
print(num ==10)
#给变量重新赋值
num = 20
print(num)
num += 120
num -= 20
num *= 100
num **= 0.5
print(num)

#1.除了=外，其余的赋值运算符必须保证给已经存在的变量重新赋值
#2.只有=可以创建不存在的变量
#3.复合赋值运算符需要把赋值运算符右侧看成一个整体
a = 3
a *= 2+1
print(a)

